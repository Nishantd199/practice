package examofjava;

public class Arraylargestnumber {
	public static void main(String[] args) {
		for(int i=6;i>=0;i--)
		{
			for(int j=i+1;j<=6;j++)
			{
				System.out.print("*");
			}System.out.println(" ");
		}
		for(int i=0;i<=5;i++)
		{
			for(int j=i+1;j<=6;j++)
			{
				System.out.print("*");
			}System.out.println(" ");
		}
}
}

		



